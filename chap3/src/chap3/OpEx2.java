package chap3;
/*
 * 논리 연산자
 */
public class OpEx2 {

	public static void main(String[] args) {
		System.out.println(true);
		System.out.println(!true);
		int x = 1000;
		System.out.println("(x < 200) = " + (x < 200));
		System.out.println("!(x < 200) = " + !(x < 200));
		System.out.println("(1 != 1) = " + (1 != 1));
		System.out.println("(1 != 2) = " + (1 != 2));
	}

}
